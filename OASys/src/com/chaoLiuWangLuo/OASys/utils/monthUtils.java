package com.chaoLiuWangLuo.OASys.utils;

public class monthUtils {
	/**部门名称*/
	private String deptName;
	
	/**用户名*/
	private String userName;
	
	/**正常 */
	private Integer normal;
	/**迟到 */
	private Integer beLate;
	/**早退*/
	private Integer leaveEarly;
	/**请假 */
	private Integer leave;
	/**出差 */
	private Integer evection;
	/**旷工*/
	private Integer absenteeism;
	public String getDeptName() {
		return deptName;
	}
	public void setDeptName(String deptName) {
		this.deptName = deptName;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public Integer getNormal() {
		return normal;
	}
	public void setNormal(Integer normal) {
		this.normal = normal;
	}
	public Integer getBeLate() {
		return beLate;
	}
	public void setBeLate(Integer beLate) {
		this.beLate = beLate;
	}
	public Integer getLeaveEarly() {
		return leaveEarly;
	}
	public void setLeaveEarly(Integer leaveEarly) {
		this.leaveEarly = leaveEarly;
	}
	public Integer getLeave() {
		return leave;
	}
	public void setLeave(Integer leave) {
		this.leave = leave;
	}
	public Integer getEvection() {
		return evection;
	}
	public void setEvection(Integer evection) {
		this.evection = evection;
	}
	public Integer getAbsenteeism() {
		return absenteeism;
	}
	public void setAbsenteeism(Integer absenteeism) {
		this.absenteeism = absenteeism;
	}
	
	
}
